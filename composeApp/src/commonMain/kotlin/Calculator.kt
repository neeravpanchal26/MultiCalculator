class Calculator {
    fun Add(left: Int, right: Int): Int {
        return left + right
    }

    fun subtract(left: Double, right: Double): Double {
        return left - right
    }

    fun multiply(left: Double, right: Double): Double {
        return left * right
    }

    fun divide(left: Double, right: Double): Double {
        return left / right
    }
}